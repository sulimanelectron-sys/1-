class QuizGame(BoxLayout):
    def __init__(self, **kwargs):
        super().__init__(orientation='vertical', padding=20, spacing=10, **kwargs)
        Window.clearcolor = (1, 1, 1, 1)
        self.index = 0
        self.score = 0
        self.brain_count = 3
        self.time_left = 10
        self.timer_event = None
        self.sound_enabled = True
        self.shuffled_questions = []
        self.correct_option_index = None
        self.answered = False
        self.show_welcome()

    def reshape_text(self, text):
        reshaped = arabic_reshaper.reshape(text)
        return get_display(reshaped)

    def play_sound(self, filename):
        if self.sound_enabled:
            sound = SoundLoader.load(filename)
            if sound:
                sound.play()

    def show_welcome(self):
        self.clear_widgets()
        logo = Image(source='تحدي الثقافة.png', size_hint=(1, None),
                     height=Window.height * 0.75, allow_stretch=True, keep_ratio=True)
        self.add_widget(logo)

        start_btn = Button(text=self.reshape_text("ابدأ اللعبة"), font_name='ArabicFont', font_size='20sp',
                           background_color=(0.2, 0.6, 0.8, 1))
        start_btn.bind(on_press=self.start_game)
        self.add_widget(start_btn)

        sound_btn = Button(text=self.reshape_text("🔊 الصوت: يعمل"), font_name='ArabicFont', font_size='20sp',
                           background_color=(0.6, 0.6, 0.6, 1))
        sound_btn.bind(on_press=self.toggle_sound)
        self.add_widget(sound_btn)

    def toggle_sound(self, instance):
        self.sound_enabled = not self.sound_enabled
        instance.text = self.reshape_text("🔊 الصوت: يعمل" if self.sound_enabled else "🔇 الصوت: مكتوم")

    def start_game(self, instance):
        self.index = 0
        self.score = 0
        self.brain_count = 3
        self.shuffled_questions = random.sample(questions, len(questions))
        self.show_question()

    def show_question(self):
        self.clear_widgets()
        self.time_left = 10
        self.start_time = time.time()
        self.answered = False

        brain_layout = BoxLayout(orientation='horizontal', size_hint=(1, None), height=50)
        for i in range(self.brain_count):
            brain = Image(source='دماغ النجاح.png', size_hint_x=None, width=40)
            brain_layout.add_widget(brain)
        self.add_widget(brain_layout)

        q = self.shuffled_questions[self.index]
        question_text = self.reshape_text(q["question"])
        label = Label(text=question_text, font_name='ArabicFont', font_size='24sp',
                      halign='right', valign='top', color=(0, 0, 0, 1))
        label.bind(size=lambda instance, value: setattr(instance, 'text_size', value))
        self.add_widget(label)

        self.progress_bar = ProgressBar(max=10, value=10)
        self.add_widget(self.progress_bar)

        self.timer_label = Label(text=self.reshape_text(f"الوقت المتبقي: {self.time_left}"), font_name='ArabicFont',
                                 font_size='20sp', halign='right', color=(0, 0, 0, 1))
        self.timer_label.bind(size=lambda instance, value: setattr(instance, 'text_size', value))
        self.add_widget(self.timer_label)

        option_pairs = list(enumerate(q["options"]))
        random.shuffle(option_pairs)
        self.current_options = [text for _, text in option_pairs]
        self.correct_option_index = [i for i, (orig_i, _) in enumerate(option_pairs) if orig_i == q["answer"]][0]

        self.option_buttons = []
        for i, option in enumerate(self.current_options):
            btn = Button(text=self.reshape_text(option), font_name='ArabicFont', font_size='20sp',
                         background_color=(0.2, 0.6, 0.8, 1))
            btn.bind(on_press=lambda inst, idx=i: self.check_answer(idx))
            self.option_buttons.append(btn)
            self.add_widget(btn)

        self.timer_event = Clock.schedule_interval(self.update_timer, 1)

    def update_timer(self, dt):
        self.time_left -= 1
        self.progress_bar.value = self.time_left
        self.timer_label.text = self.reshape_text(f"الوقت المتبقي: {self.time_left}")
        self.play_sound("مؤقت.mp3")
        if self.time_left <= 0:
            self.timer_event.cancel()
            self.check_answer(-1)

    def check_answer(self, selected):
        if self.answered:
            return
        self.answered = True

        if self.timer_event:
            self.timer_event.cancel()

        elapsed = time.time() - self.start_time

        if selected == self.correct_option_index:
            self.option_buttons[selected].background_color = (0.0, 0.8, 0.0, 1)
            self.score += 1
            if elapsed <= 3 and self.brain_count < 5:
                self.brain_count += 1
                self.play_sound("احسنت.mp3")
            else:
                self.play_sound("برافو.mp3")
        else:
            if self.brain_count > 0:
                self.brain_count -= 1
            self.play_sound("للاسف.mp3")
            if selected != -1 and selected < len(self.option_buttons):
                self.option_buttons[selected].background_color = (0.8, 0.0, 0.0, 1)
            self.option_buttons[self.correct_option_index].background_color = (0.0, 0.8, 0.0, 1)

        self.index += 1
        if self.brain_count == 0:
            Clock.schedule_once(lambda dt: self.show_game_over(), 1.2)
        elif self.index >= len(self.shuffled_questions):
            Clock.schedule_once(lambda dt: self.show_result(), 1.2)
        else:
            Clock.schedule_once(lambda dt: self.show_question(), 1.2)

    def show_result(self):
        self.clear_widgets()
        level = ["ضعيف الثقافة", "بحاجة إلى تطوير", "مستوى مقبول", "مثقف جيدًا", "خبير ثقافي", "أسطورة الثقافة!"]
        msg = f"✅ انتهت اللعبة!\n🎯 نتيجتك: {self.score} من {len(self.shuffled_questions)}\n🧠 تقييمك: {level[min(self.score, len(level)-1)]}"
        label = Label(text=self.reshape_text(msg), font_name='ArabicFont', font_size='24sp',
                      halign='right', valign='top', color=(0, 0, 0, 1))
        label.bind(size=lambda instance, value: setattr(instance, 'text_size', value))
        self.add_widget(label)

        restart_btn = Button(text=self.reshape_text("🔁 إعادة اللعب"), font_name='ArabicFont', font_size='20sp',
                             background_color=(0.2, 0.6, 0.8, 1))
        restart_btn.bind(on_press=self.start_game)
        self.add_widget(restart_btn)

        exit_btn = Button(text=self.reshape_text("❌ خروج"), font_name='ArabicFont', font_size='20sp',
                          background_color=(0.8, 0.2, 0.2, 1))
        exit_btn.bind(on_press=self.exit_game)
        self.add_widget(exit_btn)

    def show_game_over(self):
        self.clear_widgets()
        self.play_sound(random.choice(["لقد خسرت.mp3", "حظ اوفر.mp3"]))

        level = ["ضعيف الثقافة", "بحاجة إلى تطوير", "مستوى مقبول", "مثقف جيدًا", "خبير ثقافي", "أسطورة الثقافة!"]
        تقييم = level[min(self.score, len(level) - 1)]

        msg = f"❌ انتهت محاولاتك!\n🎯 نتيجتك: {self.score} من {len(self.shuffled_questions)}\n🧠 تقييمك: {تقييم}"
        result_text = self.reshape_text(msg)

        label = Label(text=result_text, font_name='ArabicFont', font_size='24sp',
                      halign='right', valign='top', color=(0.8, 0, 0, 1))
        label.bind(size=lambda instance, value: setattr(instance, 'text_size', value))
        self.add_widget(label)

        restart_btn = Button(text=self.reshape_text("🔁 إعادة اللعب"), font_name='ArabicFont', font_size='20sp',
                             background_color=(0.2, 0.6, 0.8, 1))
        restart_btn.bind(on_press=self.start_game)
        self.add_widget(restart_btn)

        exit_btn = Button(text=self.reshape_text("❌ خروج"), font_name='ArabicFont', font_size='20sp',
                          background_color=(0.8, 0.2, 0.2, 1))
        exit_btn.bind(on_press=self.exit_game)
        self.add_widget(exit_btn)

    def exit_game(self, instance=None):
        App.get_running_app().stop()

class QuizApp(App):
    def build(self):
        return QuizGame()

QuizApp().run()
